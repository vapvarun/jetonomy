<?php
/**
 * Generate the hooks INDEX from the manifest — the anti-drift companion to
 * the hand-written hooks reference.
 *
 * The prose reference (02-hooks-reference.md) explains the important hooks
 * with examples; its param columns have historically drifted from source.
 * This script emits 02a-hooks-index.md: a complete, mechanically-derived
 * table of every fired hook straight from audit/manifest.json — the same
 * inventory the release gates verify against ground-truth grep. It cannot
 * drift without the manifest drifting, which the release process catches.
 *
 * Usage: php bin/gen-hooks-reference.php   (from the plugin root)
 * Run at release alongside the manifest refresh. Deterministic output.
 *
 * @package Jetonomy
 */

$root     = dirname( __DIR__ );
$manifest = json_decode( (string) file_get_contents( $root . '/audit/manifest.json' ), true );

if ( ! is_array( $manifest ) || empty( $manifest['hooks_fired'] ) ) {
	fwrite( STDERR, "gen-hooks-reference: cannot read hooks_fired from audit/manifest.json\n" );
	exit( 1 );
}

$hooks = $manifest['hooks_fired'];
usort( $hooks, static fn( $a, $b ) => strcmp( (string) ( $a['name'] ?? '' ), (string) ( $b['name'] ?? '' ) ) );

$actions = array_values( array_filter( $hooks, static fn( $h ) => 'action' === ( $h['type'] ?? '' ) ) );
$filters = array_values( array_filter( $hooks, static fn( $h ) => 'filter' === ( $h['type'] ?? '' ) ) );

/**
 * One markdown table row for a hook entry, tolerant of both manifest arg shapes.
 *
 * @param array $h Hook entry.
 * @return string
 */
function jetonomy_hook_row( array $h ): string {
	$args = '';
	if ( ! empty( $h['args_signature'] ) && is_array( $h['args_signature'] ) ) {
		$args = implode( ', ', $h['args_signature'] );
	} elseif ( ! empty( $h['args'] ) && is_string( $h['args'] ) ) {
		$args = $h['args'];
	} elseif ( isset( $h['args_count'] ) ) {
		$args = $h['args_count'] . ' args';
	}
	$where = (string) ( $h['where'] ?? '' );
	// Escape pipes so hook arg strings can't break the table.
	$clean = static fn( string $s ): string => str_replace( '|', '\\|', trim( $s ) );
	return sprintf( "| `%s` | %s | %s |\n", $h['name'], $clean( $args ), $clean( $where ) );
}

$version = (string) ( $manifest['plugin']['version'] ?? '' );
$out     = "# Hooks Index (generated)\n\n";
$out    .= "> **Do not edit this file.** It is generated from [`audit/manifest.json`](../../../audit/manifest.json) by `bin/gen-hooks-reference.php` and regenerated at every release, so it cannot drift from the code. For explanations and copy-paste examples, use the hand-written [Hooks Reference](./02-hooks-reference.md); when the two disagree on names or arguments, THIS file wins.\n\n";
$out    .= sprintf( "Plugin version: %s — %d actions, %d filters.\n\n", $version, count( $actions ), count( $filters ) );

$out .= "## Actions (" . count( $actions ) . ")\n\n| Hook | Arguments | Fired from |\n|---|---|---|\n";
foreach ( $actions as $h ) {
	$out .= jetonomy_hook_row( $h );
}

$out .= "\n## Filters (" . count( $filters ) . ")\n\n| Hook | Arguments | Fired from |\n|---|---|---|\n";
foreach ( $filters as $h ) {
	$out .= jetonomy_hook_row( $h );
}

$dest = $root . '/docs/website/developer-guide/02a-hooks-index.md';
file_put_contents( $dest, $out );
printf( "gen-hooks-reference: wrote %s (%d actions, %d filters)\n", $dest, count( $actions ), count( $filters ) );
